test("fmtRs helper: positive", () => {
  assert.equal(reports._internal.fmtRs(100000), "₹1,00,000");
  assert.equal(reports._internal.fmtRs(0), "₹0");
});

test("fmtRs helper: negative", () => {
  assert.equal(reports._internal.fmtRs(-50000), "-₹50,000");
});

test("fmtRs helper: null / undefined / NaN → ₹0", () => {
  assert.equal(reports._internal.fmtRs(null), "₹0");
  assert.equal(reports._internal.fmtRs(undefined), "₹0");
  assert.equal(reports._internal.fmtRs(NaN), "₹0");
});

test("pct helper", () => {
  assert.equal(reports._internal.pct(0.10), "10.00%");
  assert.equal(reports._internal.pct(0.0), "0.00%");
  assert.equal(reports._internal.pct(NaN), "0%");
});

